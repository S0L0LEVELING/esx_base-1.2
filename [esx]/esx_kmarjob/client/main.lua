--[[zak op je kop]]
function dichtbij() --This function send to server closestplayer

	local closestPlayer, closestDistance = ESX.Game.GetClosestPlayer()
	local player = GetPlayerPed(-1)
	
	if closestPlayer == -1 or closestDistance > 2.0 then 
        ESX.ShowNotification('~r~Er zijn geen spelers dichtbij')
	else
	  if not HaveBagOnHead then
		TriggerServerEvent('kmarr:sendclosest', GetPlayerServerId(closestPlayer))
        ESX.ShowNotification('~g~Je hebt een zak over zijn hoofd gedaan ~w~' .. GetPlayerName(closestPlayer))
		TriggerServerEvent('kmarr:closest')
	  else
        ESX.ShowNotification('~r~Deze speler heeft al een zak overzijn hoofd')
	  end
	end
	
	end
	
	RegisterNetEvent('kmarr:menu') --This event open menu
	AddEventHandler('kmarr:menu', function()
		OpenBagMenu()
	end)
	
	RegisterNetEvent('kmarr:opdoen') --This event put head bag on nearest player
	AddEventHandler('kmarr:opdoen', function(gracz)
		local playerPed = GetPlayerPed(-1)
		zak = CreateObject(GetHashKey("prop_money_bag_01"), 0, 0, 0, true, true, true) -- Create head bag object!
		AttachEntityToEntity(zak, GetPlayerPed(-1), GetPedBoneIndex(GetPlayerPed(-1), 12844), 0.2, 0.04, 0, 0, 270.0, 60.0, true, true, false, true, 1, true) -- Attach object to head
		SetNuiFocus(false,false)
		SendNUIMessage({type = 'openGeneral'})
		HaveBagOnHead = true
	end)    
	
	AddEventHandler('playerSpawned', function() --This event delete head bag when player is spawn again
	DeleteEntity(zak)
	SetEntityAsNoLongerNeeded(zak)
	SendNUIMessage({type = 'closeAll'})
	HaveBagOnHead = false
	end)
	
	RegisterNetEvent('kmarr:afdoens') --This event delete head bag from player head
	AddEventHandler('kmarr:afdoens', function(gracz)
        ESX.ShowNotification('~g~Iemand heeft de zak van je hoofd gehaald!')
		DeleteEntity(zak)
		SetEntityAsNoLongerNeeded(zak)
		SendNUIMessage({type = 'closeAll'})
		HaveBagOnHead = false
	end)
	
	function OpenBagMenu() --This function is menu function
	
		local elements = {
			{label = 'Zet zak op hoofd', value = 'opdoen'},
			{label = 'Haal zak van hoofd', value = 'afdoen'},
			  
			}
	  
		ESX.UI.Menu.CloseAll()
	  
		ESX.UI.Menu.Open(
		  'default', GetCurrentResourceName(), 'headbagging',
		  {
			title    = 'Zak menu',
            align    = 'bottom-right',
			elements = elements
			},
	  
				function(data2, menu2)
	  
	  
				  local player, distance = ESX.Game.GetClosestPlayer()
	  
				  if distance ~= -1 and distance <= 2.0 then
	  
					if data2.current.value == 'opdoen' then
						dichtbij()
					end
	  
					if data2.current.value == 'afdoen' then
					  TriggerServerEvent('kmarr:afdoen')
					end
				  else
                    ESX.ShowNotification('~r~Er is geen speler dichtbij.')
				  end
				end,
		  function(data2, menu2)
			menu2.close()
		  end
		)
	  
	  end
